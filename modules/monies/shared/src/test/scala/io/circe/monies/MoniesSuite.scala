package io.circe.monies

import cats.kernel.Eq

import io.circe.Decoder
import io.circe.testing.CodecTests
import io.circe.tests.CirceSuite
import io.circe.parser._
import io.circe.syntax._

import org.scalacheck.{Arbitrary, Gen}

import money.{Currency, Money}

class MoniesSuite extends CirceSuite with MoniesArbitraries {

  checkLaws(
    "Codec[Money]",
    CodecTests[Money].codec
  )

  "A money decoder" should "refuse to decode unsupported currencies" in {
    val m = Money(100.0, Currency("GBP"))
    val btc = """{"amount":100.0,"currency":"BTC"}"""

    assert(Decoder[Money].decodeJson(m.asJson).isRight)
    assert(Decoder[Money].decodeJson(parse(btc).right.get).isLeft)
  }
}

